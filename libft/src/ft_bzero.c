#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*s2;

	s2 = (char *)s;
	i = 0;
	if (n == 0)
		return (0);
	while (i < n)
	{
		s2[i] = 0;
		i++;
	}
	return (s);
}
